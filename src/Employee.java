public class Employee {
    private String empName;
    private int empId;

    public void setName(String name) {
        this.empName=name;
    }

    public String getName() {
        return empName;
    }

    public void setId(int Id) {
        this.empId=Id;
    }

    public int getId() {
        return empId;
    }
}
